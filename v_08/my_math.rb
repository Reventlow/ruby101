# frozen_string_literal: true
system 'clear' # clear the terminal screen.

# Integer
puts 10 + 5 # => 15
puts 10 - 5 # => 5
puts 10 * 5 # => 50
puts 10 / 3 # => 3
puts 10 % 5 # => 0
puts 10**5 # => 100000

# Float
puts 10.0 + 5.0 # => 15.0
puts 10.0 - 5.0 # => 5.0
puts 10.0 * 5.0 # => 50.0
puts 10.0 / 3 # => 3.3333333333333335
puts 10.0 % 5.0 # => 0.0
puts 10.0**5.0 # => 100000.0
